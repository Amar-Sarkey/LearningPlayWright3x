//logical operators

/* && (AND) operator: returns true if both operands are true
   || (OR) operator: returns true if at least one operand is true
   ! (NOT) operator: returns true if the operand is false and vice versa
*/

// == looses comparison
console.log(5 == "5"); //true , checks value only
console.log(5 === "5"); //false , checks value and type


console.log("" == 0); //true , checks value only
console.log("" === 0); //false , checks value and type  
console.log("" == "0"); //false , checks value only
console.log("" === "0"); //false , checks value and type